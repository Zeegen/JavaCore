package com.javarush.task.task15.task1519;

import java.io.BufferedReader;
import java.io.InputStreamReader;
import java.io.IOException;

/* 
Разные методы для разных типов
*/

public class Solution {
    public static void main(String[] args) throws IOException {
        //напиште тут ваш код
        BufferedReader reader = new BufferedReader(new InputStreamReader(System.in));
        while (true) {
            String worlds = reader.readLine();
           if (worlds.equalsIgnoreCase("exit")) break;
           try {
               if (worlds.contains("."))print(Double.parseDouble(worlds));
               else if (Integer.parseInt(worlds) > 0 & Integer.parseInt(worlds) < 128) print((short) Integer.parseInt(worlds));
               else print(Integer.parseInt(worlds));
           }
           catch (Exception e) {print(worlds);}
        }
    }

    public static void print(Double value) {
        System.out.println("Это тип Double, значение " + value);
    }

    public static void print(String value) {
        System.out.println("Это тип String, значение " + value);
    }

    public static void print(short value) {
        System.out.println("Это тип short, значение " + value);
    }

    public static void print(Integer value) {
        System.out.println("Это тип Integer, значение " + value);
    }
}
